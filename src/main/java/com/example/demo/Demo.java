package com.example.demo;

import org.apache.ibatis.annotations.Mapper;

@Mapper
public class Demo {
    private long id;
    private String name;

    public void setId(long id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
